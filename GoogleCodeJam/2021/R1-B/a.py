import sys; import math; 
CASE=1
ITAC=0



def solve() : 
    
    return -1*CASE

def putcas(casnum, eol='\n') : 
    print('Case #'+str(casnum)+':', end=eol)

cas=0; cases=1

if CASE==1 : cases=int(input())

while True : 
    cas+=1; 
    if CASE==1 and ITAC==0 : putcas(cas, ' ')
    
    
    
    if solve()==1 : break
    
    
    
    if CASE==1 and cas>=cases : break

